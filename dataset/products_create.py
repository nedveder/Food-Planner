import pandas as pd
import random
from typing import List, Tuple, Dict
from datetime import datetime, timedelta


def generate_product_data(num_rows: int,
                          products_units: List[Tuple[str, str]],
                          unit_values: Dict[str, List[int]],
                          start_date: datetime, end_date: datetime,
                          output_file: str) -> None:
    """
    Generate a dataset of product information and save it to an Excel file.
    :param num_rows: Number of rows of data to generate.
    :param products_units: List of tuples where each tuple contains a
           product name and its unit of measurement.
    :param unit_values: Dictionary where keys are units of measurement and
                        values are lists of possible quantities
                        for each unit.
    :param start_date: The start date for the date range.
    :param end_date: The end date for the date range.
    :param output_file: The path to the output Excel file where
                        dataset will be saved.
    :return: None.
    """
    # Product quantity units used in the dataset.
    units = ['ml', 'gr', 'pieces']
    # Create a list to hold the data.
    data = []
    # Generate a list of dates included in [start_date, end_date].
    date_range = [start_date + timedelta(days=x) for x in
                  range((end_date - start_date).days + 1)]
    for i in range(num_rows):
        # Randomly select a product and unit.
        product_name, unit = random.choice(products_units)
        # Randomly select a quantity based on the units.
        quantity = random.choice(unit_values[unit])
        # Randomly select a date from the date range.
        date = random.choice(date_range)
        # Append the data to the list.
        data.append([i + 1, product_name, quantity, unit,
                     date])
    # Create a pandas DataFrame from the data.
    df = pd.DataFrame(data, columns=['ID', 'Product Name', 'Quantity',
                                     'Unit', 'Date'])
    # Save the DataFrame to an Excel file.
    df.to_excel(output_file, index=False)
    print(f"Data saved to {output_file}")


